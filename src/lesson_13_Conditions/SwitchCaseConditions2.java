package lesson_13_Conditions;

public class SwitchCaseConditions2 {

	public static void main(String[] args) {
		
		String color = "White";
		
		switch(color)
		{
		
		case "Saffron":
		System.out.println("YES");
		break;
		
		case "White":
		System.out.println("YES");
		break;
		
		case "Green":
		System.out.println("YES");
		break;
			
		case "Blue":
		System.out.println("YES");
		break;
			
		default:
		System.out.println("NO");
		
		
		}
		

	}

}
