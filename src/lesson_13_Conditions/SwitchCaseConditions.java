package lesson_13_Conditions;

public class SwitchCaseConditions {

	public static void main(String[] args) {
		
		String day = "Sunday";
		
		switch(day)
		{
		
		case "Monday":
		System.out.println("It is Day 1");
		break;
		
		case "Tuesday":
		System.out.println("It is Day 2");
		break;
		
		case "Wednesday":
		System.out.println("It is Day 3");
		break;
			
		case "Thursday":
		System.out.println("It is day 4");
		break;
			
		case "Friday":
		System.out.println("It is day 5");
		break;
		
		default:
		System.out.println("Dont know the day");
		
		
		}
		

	}

}
